
/**
 * Class Player - player status in an adventure game.
 * 
 * Player needs energy to move through the rooms.
 *
 * @author Jose Gomes
 * @version 24/01/2021
 */
public class Player
{
    // instance variables 
    public int energy = 100;
    public int score = 0;

    /**
     * Constructor for objects of class Player
     */
    public Player()
    {
        // initialise instance variables
        this.energy=energy;
        this.score=score;
    }
}
